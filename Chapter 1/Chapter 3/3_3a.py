# Selection statements allow a computer to make choices based on a condition

# Before you can test conditions in a Python program, you need to 
# understand the Boolean data type.
# In Python, the two Boolean values are True and False. Capitalization matters!
# Capital T or capital F

#The condition in a selection statement often takes the form
# of a comparison. The result of the comparison is a Boolean value.

print(4 == 4) #True
print(4 == 5) #False
print(4 != 5) #True # != means not equal to
print(4 < 5) #True
print(4 > 5) #False
print(4 <= 5) #True
print(4 >= 5) #False

print("A" < "B") #True
print("A" > "B") #False
print("A" == "B") #False
print("A" != "B") #True

