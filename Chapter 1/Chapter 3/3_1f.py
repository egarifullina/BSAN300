# augmented assignment operations - combining the assgnment symbol with arithmetic and concatenation operators
a = 17
s = "hi"

a += 3 # equivalent to a = a + 3
print(a)

a -= 3 # equivalent to a = a - 3
print(a)

a *= 3 # equivalent to a = a * 3
print(a)

a /= 3 # equivalent to a = a / 3
print(a)

a %= 3 # equivalent to a = a % 3
print(a)

s += " there" # equivalent to s = s + " there"
print(s)